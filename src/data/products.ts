export const getMixedProducts = (limit?: number): Product[] => {
  const mixed: Product[] = [];
  const categoryMap = new Map<string, Product[]>();
  
  allProducts.forEach(p => {
    if (!categoryMap.has(p.category)) categoryMap.set(p.category, []);
    categoryMap.get(p.category)!.push(p);
  });

  const categories = Array.from(categoryMap.keys());
  let index = 0;
  
  while (true) {
    let added = false;
    for (const cat of categories) {
      const items = categoryMap.get(cat)!;
      if (index < items.length) {
        mixed.push(items[index]);
        added = true;
        if (limit && mixed.length === limit) return mixed;
      }
    }
    if (!added) break;
    index++;
  }
  
  return mixed;
};
